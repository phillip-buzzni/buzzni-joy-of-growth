// 측정 가능한 행동 지표 추출
export function getBehaviorMetrics(principleId, level) {
  const principle = leadershipPrinciples.find((p) => p.id === principleId);
  if (!principle) return [];

  const behavior = principle.behaviors.find((b) => b.level === level);
  return behavior ? behavior.measurements : [];
}

// 특정 레벨의 모든 원칙 행동 지표
export function getAllBehaviorsByLevel(level) {
  return leadershipPrinciples
    .map((principle) => ({
      principleId: principle.id,
      principleName: principle.name,
      behavior: principle.behaviors.find((b) => b.level === level),
    }))
    .filter((item) => item.behavior);
}

// 안티패턴 체크리스트
export function getAntiPatterns(principleId) {
  const principle = leadershipPrinciples.find((p) => p.id === principleId);
  return principle ? principle.antiPatterns : [];
}

